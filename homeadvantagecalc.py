def calculate_home_advantage(matches_data):
    home_team_wins = defaultdict(int)
    home_team_losses = defaultdict(int)

    for match in matches_data:
        home_team, away_team, home_result, away_result = match
        if home_result == 'W':
            home_team_wins[home_team] += 1
            home_team_losses[away_team] += 1
        elif home_result == 'L':
            home_team_losses[home_team] += 1
            home_team_wins[away_team] += 1

    home_advantage = {}
    for team in home_team_wins:
        total_home_games = home_team_wins[team] + home_team_losses[team]
        home_win_percentage = home_team_wins[team] / total_home_games
        home_advantage[team] = 1 + home_win_percentage  # Adjusted to ensure value falls between 1 and 2

    return home_advantage

home_advantage = calculate_home_advantage(matches_data)

